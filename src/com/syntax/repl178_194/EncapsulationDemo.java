package com.syntax.repl178_194;

class EncapsulationDemo {
	private String empName;
	private int empAge;

	void setEmpName(String empName) {
		this.empName = empName;
	}

	void setEmpAge(int empAge) {
		this.empAge = empAge;
	}

	String getEmpName() {
		return empName;
	}

	int getEmpAge() {
		return empAge;
	}

}
